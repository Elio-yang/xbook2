/**
 * gui_mouse_draw_buffer - 往缓冲区里面绘制鼠标
 */
static void gui_mouse_draw_buffer(GUI_COLOR *buffer, char *data)
{
    int x, y;
	for (y = 0; y < gui_mouse.height; y++) {
		for (x = 0; x < gui_mouse.width; x++) {
			if (data[y * gui_mouse.width + x] == 0) {
                /* 透明色 */
                buffer[y * gui_mouse.width + x] = COLOR_ARGB(0, 0, 0, 0);
            } else if (data[y * gui_mouse.width + x] == 1) {
				/* 边框色 */
                buffer[y * gui_mouse.width + x] = COLOR_WHITE;
            } else if (data[y * gui_mouse.width + x] == 2) {
                /* 填充色 */
                buffer[y * gui_mouse.width + x] = COLOR_BLACK;
            }
		}
	}
}

/**
 * 根据鼠标的状态绘制鼠标
 * 
 */
void gui_mouse_draw(mouse_state_t state)
{
    switch (state)
    {
    case MOUSE_NORMAL:
        gui_mouse_draw_buffer(gui_mouse.layer->buffer, __mouse_cursor_normal);
        gui_mouse_set_lyoff(0, 0);
        break;
    case MOUSE_HOLD:
        gui_mouse_draw_buffer(gui_mouse.layer->buffer, __mouse_cursor_hold);
        gui_mouse_set_lyoff(0, 0);
        break;
    case MOUSE_VRESIZE:
        gui_mouse_draw_buffer(gui_mouse.layer->buffer, __mouse_cursor_vresize);
        gui_mouse_set_lyoff(-4, -10);
        break;
    case MOUSE_HRESIZE:
        gui_mouse_draw_buffer(gui_mouse.layer->buffer, __mouse_cursor_hresize);
        gui_mouse_set_lyoff(-10, -4);
        break;
    case MOUSE_DRESIZE1:
        
        gui_mouse_draw_buffer(gui_mouse.layer->buffer, __mouse_cursor_dresize1);
        gui_mouse_set_lyoff(-7, -7);
        break;
    case MOUSE_DRESIZE2:
        
        gui_mouse_draw_buffer(gui_mouse.layer->buffer, __mouse_cursor_dresize2);
        
        gui_mouse_set_lyoff(-7, -7);
        break;
    default:
        return;
    }
    gui_mouse_layer_move();
}

void gui_mouse_set_state(mouse_state_t state)
{
    if (gui_mouse.state == state)   /* 已经是某种状态就不切换 */
        return;
    //printk("[gui]: mouse state %d -> %d\n", gui_mouse.state, state);
    gui_mouse.state = state;
    gui_mouse_draw(state);
}

void gui_mouse_set_lyoff(int x, int y)
{
    gui_mouse.lyoff.x = x;
    gui_mouse.lyoff.y = y;
}

void gui_mouse_layer_move()
{
    layer_set_xy(gui_mouse.layer, gui_mouse.x + gui_mouse.lyoff.x,
        gui_mouse.y + gui_mouse.lyoff.y);
}

