void gui_mouse_motion()
{
     /* 对鼠标进行修复 */
    if (gui_mouse.x < 0)
        gui_mouse.x = 0;
    if (gui_mouse.y < 0)
        gui_mouse.y = 0;
    if (gui_mouse.x >= gui_screen.width)
        gui_mouse.x = gui_screen.width - 1;
    if (gui_mouse.y >= gui_screen.height)
        gui_mouse.y = gui_screen.height - 1;
    
    gui_mouse_layer_move();

    g_msg_t m;
    memset(&m, 0, sizeof(g_msg_t));
    m.id        = GM_MOUSE_MOTION;
    m.data0     = gui_mouse.x;
    m.data1     = gui_mouse.y;
    gui_push_msg(&m);
}


